from __future__ import annotations

from typing import Any

from atomstudio.structure.selectors import norm_symbol_pair

PairBondDistanceRange = tuple[float, float]


def normalize_pair_key(value: str) -> str:
    raw = str(value).strip()
    if "-" not in raw:
        raise ValueError("Pair key must be an element pair like 'O-Ti'.")
    left, right = raw.split("-", 1)
    return norm_symbol_pair(left.strip(), right.strip())


def normalize_pair_distance_ranges(values: dict[str, Any]) -> dict[str, PairBondDistanceRange]:
    out: dict[str, PairBondDistanceRange] = {}
    for key, raw_range in values.items():
        norm_key = normalize_pair_key(key)
        min_distance, max_distance = _coerce_pair_distance_range(raw_range, label=norm_key)
        out[norm_key] = (min_distance, max_distance)
    return dict(sorted(out.items()))


def _coerce_pair_distance_range(value: Any, *, label: str) -> PairBondDistanceRange:
    if isinstance(value, dict):
        raw_min = value.get("min_distance", value.get("min", 0.0))
        raw_max = value.get("max_distance", value.get("max"))
    elif isinstance(value, (list, tuple)) and len(value) == 2:
        raw_min, raw_max = value
    else:
        raise ValueError(f"Pair distance range for {label} must be [min, max] or a mapping with min/max distance.")
    if raw_max is None:
        raise ValueError(f"Pair distance range for {label} requires max_distance.")
    min_distance = float(raw_min)
    max_distance = float(raw_max)
    if min_distance < 0.0:
        raise ValueError(f"Pair minimum distance for {label} must be >= 0.")
    if max_distance <= min_distance:
        raise ValueError(f"Pair maximum distance for {label} must be > minimum distance.")
    return min_distance, max_distance


